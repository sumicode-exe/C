#include <stdio.h>

int main() {
    printf("Hello\tWorld!\n");  // \t is used for horizontal tab
    printf("This is a new line.\n");  // \n is used for new line
    printf("Backslash: \\ \n");  // To print a backslash, you need to use \\
    printf("Double quotes: \"Hello!\" \n");  // To print double quotes, you need to use \"

    return 0;
}